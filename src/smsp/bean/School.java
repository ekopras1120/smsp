package smsp.bean;

public class School {
	private int schoolId;
	private String schoolName;
	private String schoolAddress;
	private int schoolTelp;
	private int schoolPostCode;
	
	public int getSchoolId() {
		return schoolId;
	}
	public void setSchoolId(int schoolId) {
		this.schoolId = schoolId;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolAddress() {
		return schoolAddress;
	}
	public void setSchoolAddress(String schoolAddress) {
		this.schoolAddress = schoolAddress;
	}
	public int getSchoolTelp() {
		return schoolTelp;
	}
	public void setSchoolTelp(int schoolTelp) {
		this.schoolTelp = schoolTelp;
	}
	public int getSchoolPostCode() {
		return schoolPostCode;
	}
	public void setSchoolPostCode(int schoolPostCode) {
		this.schoolPostCode = schoolPostCode;
	}
	

}
